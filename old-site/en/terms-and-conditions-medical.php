                                mogelijk – worden afgeweken van deze toestemmingsprocedure, conform
                                wettelijke bepalingen.</p>

                            <h2 class="wow fadeInUp">Artikel 7 – Medisch dossier</h2>
                            <p class="wow fadeInUp">De kliniek houdt een medisch dossier bij met gegevens over de gezondheid,
                                verrichtingen en relevante documenten.</p>
                            <p class="wow fadeInUp">Op verzoek voegt de kliniek een schriftelijke verklaring van de patiënt toe.</p>
                            <p class="wow fadeInUp">Het dossier wordt minimaal vijftien jaar bewaard, zoals wettelijk voorgeschreven.</p>
