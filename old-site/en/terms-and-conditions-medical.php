                            <p class="wow fadeInUp">Op verzoek worden gegevens binnen drie maanden vernietigd, tenzij de wet zich
                                hiertegen verzet of gegevens van belang zijn voor derden.</p>
                            <p class="wow fadeInUp">Persoonsgegevens worden verwerkt conform de geldende wetgeving, waaronder de
                                AVG. Deze gegevens worden uitsluitend gebruikt voor de uitvoering van de
                                behandeling en niet gedeeld met derden zonder toestemming, tenzij wettelijk
                                verplicht.</p>

                            <h2 class="wow fadeInUp">Artikel 8 – Aansprakelijkheid</h2>
                            <p class="wow fadeInUp">De kliniek en/of specialist zijn aansprakelijk voor schade als gevolg van
                                tekortkomingen bij alle handelingen die onder deze overeenkomst binnen de kliniek
                                plaatsvinden.</p>
                            <p class="wow fadeInUp">Er is <b>geen beperking of uitsluiting</b> van aansprakelijkheid.</p>

                            <h2 class="wow fadeInUp">Artikel 9 – Betaling</h2>
