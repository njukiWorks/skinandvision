                                akkoord in het digitale dossier.</p>

                            <p class="wow fadeInUp">De patiënt heeft gelegenheid gehad om vragen te stellen en verklaart door akkoord
                                te gaan voldoende te zijn geïnformeerd.</p>

                            <h2 class="wow fadeInUp">Artikel 3 – Inlichtingen- en medewerkingsplicht van de patiënt</h2>
                            <p class="wow fadeInUp">De patiënt verstrekt alle informatie die redelijkerwijs nodig is voor een goede
                                behandeling en werkt zoveel mogelijk mee door adviezen en voorschriften op te
                                volgen.</p>
                            <h2 class="wow fadeInUp">Artikel 4 – Informatieplicht van de specialist en de kliniek</h2>
                            <p class="wow fadeInUp">De kliniek en/of specialist informeert de patiënt op begrijpelijke wijze, en
                                desgevraagd schriftelijk, over:</p>
                            <ul class="wow fadeInUp">
                                <li>het noodzakelijke onderzoek en/of behandeling,</li>
                                <li>de uit te voeren verrichtingen,</li>
                                <li>de te verwachten gevolgen en risico’s,</li>
                                <li>alternatieve behandelmethoden,</li>
                                <li>het beoogde resultaat.</li>
                            </ul>
                            <p class="wow fadeInUp">Indien het verstrekken van informatie ernstig nadeel voor de patiënt zou opleveren,
