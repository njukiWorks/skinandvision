                            <h2 class="wow fadeInUp">Artikel 5 – Geheimhouding</h2>
                            <p class="wow fadeInUp">De kliniek en specialist verstrekken zonder toestemming van de patiënt geen
                                informatie of inzage aan derden, tenzij wettelijk verplicht.
                                Medische verrichtingen vinden buiten waarneming van anderen plaats, tenzij de
                                patiënt hier nadrukkelijk mee instemt.</p>

                            <h2 class="wow fadeInUp">Artikel 6 – Toestemming van/namens de patiënt</h2>
                            <p class="wow fadeInUp">De patiënt geeft op basis van de verstrekte informatie toestemming voor de
                                besproken behandeling, zoals vastgelegd in het dossier.</p>
                            <p class="wow fadeInUp">Deze toestemming omvat het inschakelen van andere deskundigen indien
                                noodzakelijk, ongeacht het specialisme.</p>
                            <p class="wow fadeInUp">Toestemming kan altijd worden ingetrokken.</p>
                            <p class="wow fadeInUp">Indien tijdens de behandeling een ingrijpender ingreep nodig blijkt, wordt hiervoor
                                altijd eerst aanvullende toestemming gevraagd, tenzij de patiënt vooraf een
                                gemachtigde heeft aangewezen.</p>
                            <p class="wow fadeInUp">Op verzoek wordt schriftelijk vastgelegd voor welke ingrepen toestemming is
                                verleend.</p>
