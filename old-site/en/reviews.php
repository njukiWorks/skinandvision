    <script>
        // Star rating interaction
        const stars = document.querySelectorAll('#starRating .star');
        const ratingInput = document.getElementById('ratingValue');
        let selectedRating = 5;

        stars.forEach(star => {
            star.addEventListener('mouseover', function() {
                const val = parseInt(this.dataset.value);
                stars.forEach(s => {
                    s.style.color = parseInt(s.dataset.value) <= val ? '#f5a623' : '#ccc';
                });
            });
            star.addEventListener('mouseout', function() {
                stars.forEach(s => {
                    s.style.color = parseInt(s.dataset.value) <= selectedRating ? '#f5a623' : '#ccc';
                });
            });
            star.addEventListener('click', function() {
                selectedRating = parseInt(this.dataset.value);
                ratingInput.value = selectedRating;
                stars.forEach(s => {
                    s.style.color = parseInt(s.dataset.value) <= selectedRating ? '#f5a623' : '#ccc';
                });
            });
        });

        // Load reviews
        function loadReviews() {
            fetch('/reviews.json')
                .then(r => r.json())
                .then(reviews => {
                    const grid = document.getElementById('reviewsGrid');
                    if (!reviews.length) {
                        grid.innerHTML = '<div class="col-12 text-center"><p>No reviews yet. Be the first!</p></div>';
                        return;
                    }
                    grid.innerHTML = reviews.slice().reverse().map(r => {
                        const filled = '&#9733;'.repeat(r.rating);
                        const empty = '&#9734;'.repeat(5 - r.rating);
                        return `<div class="col-lg-4 col-md-6 mb-4">
                        <div class="contact-info-item wow fadeInUp" style="display:block; padding:1.8rem; border-radius:12px; box-shadow:0 4px 24px rgba(0,0,0,0.07); height:100%;width: auto;">
                            <div style="color:#f5a623; font-size:1.3rem; margin-bottom:0.75rem;">${filled}${empty}</div>
                            <p style="margin-bottom:1rem; line-height:1.7;">${r.review}</p>
                            <hr style="margin:1rem 0; opacity:0.15;">
                            <strong>${r.name}</strong><br>
                            <small class="text-muted">${r.date}</small>
                        </div>
                    </div>`;
                    }).join('');
                })
                .catch(() => {
                    document.getElementById('reviewsGrid').innerHTML = '<div class="col-12 text-center"><p>Could not load reviews.</p></div>';
                });
        }
        loadReviews();

        // Submit review
        document.getElementById('reviewForm').addEventListener('submit', function(e) {
            e.preventDefault();
            const msg = document.getElementById('reviewMessage');
            const btn = this.querySelector('button[type="submit"]');
            btn.disabled = true;
            const formData = new FormData(this);
            fetch('/submit_review.php', {
                    method: 'POST',
                    body: formData
                })
                .then(r => r.json())
                .then(data => {
                    if (data.success) {
                        msg.innerHTML = '<div class="alert alert-success">Thank you for your review! It is now visible on the page.</div>';
                        this.reset();
                        selectedRating = 5;
                        ratingInput.value = 5;
                        stars.forEach(s => {
                            s.style.color = '#f5a623';
                        });
                        loadReviews();
                    } else {
                        msg.innerHTML = '<div class="alert alert-danger">Something went wrong. Please try again.</div>';
                    }
                    btn.disabled = false;
                })
                .catch(() => {
                    msg.innerHTML = '<div class="alert alert-danger">Connection error. Please try again.</div>';
                    btn.disabled = false;
                });
        });
    </script>

