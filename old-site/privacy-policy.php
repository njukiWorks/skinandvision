                            <p class="wow fadeInUp">Skin &amp; Vision Clinics B.V. hecht grote waarde aan de bescherming van uw
                                persoonsgegevens. In deze privacyverklaring informeren wij u helder en transparant
                                over hoe wij met uw gegevens omgaan. Deze verklaring is van toepassing op alle
                                cliënten, zowel voor verzekerde medische zorg als voor cosmetische behandelingen.</p>
                            <h2 class="wow fadeInUp">1. Wat zijn persoonsgegevens?</h2>
                            <p class="wow fadeInUp">Persoonsgegevens zijn alle gegevens die direct of indirect tot u als individu
                                herleidbaar zijn, zoals naam, geboortedatum, adres, e-mailadres, telefoonnummer en
                                gezondheidsgegevens. Wij verwerken deze gegevens wanneer u bij ons een
                                behandeling ondergaat of contact met ons opneemt.</p>
                            <h2 class="wow fadeInUp">2. Welke gegevens verwerken wij?</h2>
                            <p class="wow fadeInUp">Wij verwerken de volgende categorieën persoonsgegevens:</p>
                            <ul class="wow fadeInUp">
                                <li>Identificatiegegevens (zoals naam, adres, telefoonnummer, e-mailadres,
                                    geboortedatum);</li>
                                <li>Medische gegevens die relevant zijn voor uw behandeling;</li>
                                <li>Verwijsbrieven en medische voorgeschiedenis (indien van toepassing);</li>
                                <li>Financiële gegevens ten behoeve van declaratie en facturatie;</li>
                                <li>Communicatiegegevens (zoals e-mailcontact, ingevulde vragenlijsten,
                                    toestemmingsverklaringen);</li>
                                <li>Overige gegevens die u actief aan ons verstrekt (bijvoorbeeld bij
                                    contactformulieren of klachtenprocedures).</li>

                            </ul>
                            <h2 class="wow fadeInUp">3. Doeleinden en grondslagen van verwerking</h2>
                            <p class="wow fadeInUp">Wij verwerken uw persoonsgegevens uitsluitend op basis van een geldige grondslag
                                volgens de Algemene Verordening Gegevensbescherming (AVG):</p>

                            <ul class="wow fadeInUp">
                                <li>Uitvoering van de behandelovereenkomst (zowel medisch als cosmetisch);</li>
                                <li>Uw uitdrukkelijke toestemming (bijvoorbeeld voor het delen van gegevens met
                                    derden zoals verwijzers of apothekers; deze toestemming wordt schriftelijk of
                                    digitaal vastgelegd);</li>
                                <li>Het voldoen aan wettelijke verplichtingen (zoals de WGBO, Wkkgz en fiscale
                                    bewaarplicht);</li>
                                <li>Gerechtvaardigd belang, uitsluitend indien de verwerking niet op een andere
                                    grondslag kan worden gebaseerd (bijvoorbeeld voor kwaliteitsbewaking en
                                    interne audits, met afweging van uw privacybelang).</li>
                                <li>Communicatiegegevens (zoals e-mailcontact, ingevulde vragenlijsten,
                                    toestemmingsverklaringen);</li>

                            </ul>

                            <h2 class="wow fadeInUp">4. Met wie delen wij uw gegevens?</h2>
                            <p class="wow fadeInUp">Uw persoonsgegevens worden alleen gedeeld met:</p>

                            <ul class="wow fadeInUp">
                                <li>Zorgverleners binnen onze kliniek die direct betrokken zijn bij uw behandeling
                                    (op basis van het ‘need-to-know’-principe);</li>
                                <li>Uw verwijzer of huisarts, uitsluitend met uw expliciete toestemming, welke wij
                                    schriftelijk/digitaal vastleggen;</li>
                                <li>Apothekers, zorgverzekeraars en administratieve verwerkers indien van
                                    toepassing en wettelijk toegestaan;</li>
                                <li>Externe dienstverleners (zoals IT-leveranciers of accountants), waarmee altijd
                                    een verwerkersovereenkomst conform AVG is gesloten;</li>
                                <li>Overheidsinstanties indien wij daartoe wettelijk verplicht zijn (zoals IGJ of de
                                    Belastingdienst).</li>
                                <p class="wow fadeInUp" style="font-style: italic;">Uw gegevens worden nooit verstrekt aan partijen buiten de Europese
                                    Economische Ruimte (EER). Indien wij gebruikmaken van cloudopslag,
                                    bevinden deze servers zich binnen de EER.</p>

                            </ul>

                            <h2 class="wow fadeInUp">5. Beveiliging van uw gegevens</h2>
                            <p class="wow fadeInUp">Wij nemen passende technische en organisatorische maatregelen om uw
                                persoonsgegevens te beschermen tegen verlies, ongeoorloofde toegang of
                                onrechtmatige verwerking, waaronder:</p>

                            <ul class="wow fadeInUp">
                                <li>Beveiligde toegang tot systemen (tweefactorauthenticatie, autorisatiebeleid);</li>
                                <li>Encryptie van opslagmedia en back-ups;</li>
                                <li>Periodieke controle en actualisatie van beveiligingsmaatregelen;</li>
                                <li>Loggen en monitoren van toegang tot medische dossiers;</li>
                                <li>Alleen bevoegde medewerkers hebben toegang tot uw gegevens, en zij zijn
                                    tot geheimhouding verplicht;</li>
                                <li>Elektronische overdracht van medische gegevens (zoals verwijzingen) gebeurt
                                    via beveiligde kanalen (bijvoorbeeld ZorgMail).</li>
                                <p class="wow fadeInUp" style="font-style: italic;">Skin &amp; Vision Clinics B.V. heeft een Functionaris Gegevensbescherming (FG)
                                    aangesteld. Voor vragen over privacy kunt u bij de FG terecht via
                                    <a href="privacy@skinandvision.nl"> privacy@skinandvision.nl</a>.
                                </p>
                            </ul>

                            <h2 class="wow fadeInUp">6. Bewaartermijn</h2>

                            <p class="wow fadeInUp">Uw persoonsgegevens worden niet langer bewaard dan strikt noodzakelijk. Voor
                                medische dossiers geldt een wettelijke bewaartermijn van 20 jaar (artikel 7:454 lid 3
                                BW), gerekend vanaf de laatste wijziging. Voor cosmetische behandelingen hanteren
                                wij een bewaartermijn van 10 jaar, tenzij op grond van wet- of regelgeving een
                                andere termijn geldt. Na afloop van de bewaartermijn worden uw gegevens veilig en
                                zorgvuldig vernietigd.</p>

                            <h2 class="wow fadeInUp">7. Uw rechten</h2>
                            <p class="wow fadeInUp">U heeft op grond van de AVG de volgende rechten:</p>
                            <ul class="wow fadeInUp">
                                <li>Recht op inzage en afschrift van uw gegevens;</li>
                                <li>Recht op correctie (rectificatie) van onjuiste of onvolledige gegevens;</li>
                                <li>Recht op verwijdering (‘recht op vergetelheid’), voor zover geen wettelijke
                                    bewaarplicht geldt;</li>
                                <li>Recht op beperking van de verwerking;</li>
                                <li>Recht op bezwaar tegen de verwerking van uw gegevens;</li>
                                <li>Recht op overdraagbaarheid van uw gegevens (dataportabiliteit).</li>
                                <p class="wow fadeInUp" style="font-style: italic;">U kunt uw verzoek per e-mail indienen via <a href="privacy@skinandvision.nl">privacy@skinandvision.nl.</a> Wij reageren
                                    uiterlijk binnen vier weken op uw verzoek.
                                </p>
                            </ul>

                            <h2 class="wow fadeInUp">8. Datalekken en incidenten</h2>
                            <p class="wow fadeInUp">Bij een beveiligingsincident (datalek) met mogelijk risico voor uw privacy melden wij
                                dit bij de Autoriteit Persoonsgegevens (AP) en, indien van toepassing, ook direct aan
                                u. Wij houden een intern register van datalekken bij en nemen passende
                                maatregelen om herhaling te voorkomen.</p>

                            <h2 class="wow fadeInUp">9. Klachten en toezicht</h2>
                            <p class="wow fadeInUp">Bent u van mening dat wij niet correct met uw persoonsgegevens omgaan, dan kunt
                                u contact opnemen met onze Functionaris Gegevensbescherming via privacy@skinandvision.nl. Daarnaast kunt u een klacht indienen bij de Autoriteit
                                Persoonsgegevens via <a href="https://www.autoriteitpersoonsgegevens.nl"> www.autoriteitpersoonsgegevens.nl</a>.</p>

